import { test } from 'qunit';
import moduleForAcceptance from 'forum/tests/helpers/module-for-acceptance';

moduleForAcceptance('Acceptance | create question');

test('visiting /create-question', function(assert) {
  visit('/question/new');
  fillIn('input.askedBy', 'Arvind');
  fillIn('input.question', 'This was generated by Test');
  click('button.submit')
  andThen(function() {
    assert.equal(currentURL(), '');
    assert.equal(find('div.question-div:last div:first h6').text(), 'This was generated by Test');
  });
});
